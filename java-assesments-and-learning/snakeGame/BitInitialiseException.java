/**
 * This method is the exception for the bit initialise error. 
 * 
 * @author (Akash Payne) ap567
 * @version 31/03/2014
 */

public class BitInitialiseException extends Exception
{
	/** 
     * This method is the exception handler that catches the exception.
     */ 
	public BitInitialiseException()
	{
		super();
	}

	/** 
     * This method is the exception handler that catches the exception.
     */ 
	public BitInitialiseException(String message)
	{
		super(message);
	}
}